#include "libft.h"
#include <stdlib.h>

void	ft_arrfree(char ***arr)
{
	int i;

	i = 0;
	while ((*arr)[i])
		free((*arr)[i++]);
	free((*arr)[i]);
	free(*arr);
	*arr = NULL;
}
