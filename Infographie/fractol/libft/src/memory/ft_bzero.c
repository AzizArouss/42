#include "libft.h"
#include <string.h>

void	ft_bzero(void *s, size_t n)
{
	size_t		i;

	i = 0;
	if (!s)
		return ;
	while (i < n)
	{
		*(char *)(s + i) = 0;
		i++;
	}
}
